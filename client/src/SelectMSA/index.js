class SearchBox extends Component {
  initialize = () => {
    const choices = formattAPIcall(data);
    return { choices: choices, defaultChoice: choices[438]};
  };
  state = this.initialize();

  componentDidMount() {
    const { onChangecallback } = this.props;
    onChangecallback("", this.state.defaultChoice);
  }

  handleClear = (event, value, reason) => {
    const { onChangecallback } = this.props;
    if (reason === "clear") {
      onChangecallback(event, { msa: "" });
    }
  };

  handleChange = (event, values) => {
    const { onChangecallback } = this.props;
    onChangecallback(event, values);
  };

  render() {
    const { classes } = this.props;
    return (
      <div className={classes.root}>
        <Autocomplete
          id="combo-box-demo"
          options={this.state.choices}
          onInputChange={this.handleClear}
          defaultValue={this.state.defaultChoice}
          onChange={this.handleChange}
          getOptionLabel={option => option.name}
          style={{ width: 425 }}
          renderInput={params => (
            <TextField
              {...params}
              label="Select an MSA to calculate this homes affordability"
              variant="outlined"
              fullWidth
            />
          )}
        />
      </div>
    );
  }
}

